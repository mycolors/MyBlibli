package com.fengniao.myblibli.bean.live;



public class Partition {

    private int id;
    private String name;
    private String area;
    private Icon sub_icon;
    private int count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public Icon getSub_icon() {
        return sub_icon;
    }

    public void setSub_icon(Icon sub_icon) {
        this.sub_icon = sub_icon;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
